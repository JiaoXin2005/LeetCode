/**
 * Definition for a binary tree node.
 * function TreeNode(val, left, right) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.left = (left===undefined ? null : left)
 *     this.right = (right===undefined ? null : right)
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number}
 */
var rob = function(root) {
  const map = new Map();
  const dfs = (root) => {
    if (!root) return 0;
    if (map.has(root)) return map.get(root);

    let baseVal = root.val;

    if (root.left) {
      baseVal += dfs(root.left.left) + dfs(root.left.right);
    }

    if (root.right) {
      baseVal += dfs(root.right.left) + dfs(root.right.right);
    }

    let notRootVal = dfs(root.left) + dfs(root.right);

    let res = Math.max(baseVal, notRootVal);

    map.set(root, res);

    return res;
  };

  return dfs(root);
};
